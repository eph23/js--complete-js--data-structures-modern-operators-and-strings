// NOTE: Logical Assignment Operators (&&=, ||=, ??=)
/* 
Logical assignment operators combine logical operators (&&, ||, ??) with assignment (=). They help in conditionally updating variables based on their current values. 
*/

// Logical OR Assignment (||=)
/* 
The ||= operator assigns a new value only if the current value is falsy (false, 0, "", null, undefined, NaN).
*/
